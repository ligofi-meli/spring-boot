package gomes.filipe.lesson2;

public class Carro {
    String marca;
    String modelo;
    int anofabricacao;
    String cor;
    double preco;

    public Carro(String marca, String modelo, int anofabricacao, String cor, double preco) {
        this.marca = marca;
        this.modelo = modelo;
        this.anofabricacao = anofabricacao;
        this.cor = cor;
        this.preco = preco;
    }
}
